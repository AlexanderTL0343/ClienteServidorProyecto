package com.mycompany.fidemotorbusiness;

/**
 *
 * @author Venta PC
 */
public class Camion extends Vehiculo {

    private String marca;
    private int ano;
    private String modelo;
    private double precio;

    public Camion(String marca, int ano, String modelo, double precio) {
        this.marca = marca;
        this.ano = ano;
        this.modelo = modelo;
        this.precio = precio;
    }

    @Override
    public String getMarca() {
        return marca;
    }

    @Override
    public void setMarca(String marca) {
        this.marca = marca;
    }

    @Override
    public int getAno() {
        return ano;
    }

    @Override
    public void setAno(int ano) {
        this.ano = ano;
    }

    @Override
    public String getModelo() {
        return modelo;
    }

    @Override
    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    @Override
    public double getPrecio() {
        return precio;
    }

    @Override
    public void setPrecio(double precio) {
        this.precio = precio;
    }

}
